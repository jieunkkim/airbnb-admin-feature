/**
 * 주소에서 시도 추출
 * @param {string} addr - 도로명 주소
 * @returns {string} 시도명
 */
function extractSidoFromAddress(addr) {
  if (!addr) return '';
  const parts = addr.split(' ');
  return parts[0] || '';
}

